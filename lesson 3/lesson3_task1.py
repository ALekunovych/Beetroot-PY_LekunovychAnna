my_name = "Anna"
day = "Friday"
print(f"Good day {my_name}! {day} is a perfect day to learn some python.")

s="Good day {}! {} is a perfect day to learn some python."
print(s.format(my_name, day))

print("Good day {0}! {1} is a perfect day to learn some python." . format (my_name, day))

print("Good day %s! %s is a perfect day to learn some python." % (my_name, day))